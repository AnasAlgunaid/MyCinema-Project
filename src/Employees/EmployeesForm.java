    public static void refreshTable(){
        String allEmployeesQuery = "SELECT Employee_ID AS `Employee ID`, Full_Name AS `Full Name`, Email, Role_Title AS `Role` FROM EmployeesInfo ";
        
        // Check for the order combobox 
        if(orderComboBox.getSelectedItem() == "Full Name: A-Z"){
            allEmployeesQuery = allEmployeesQuery + " ORDER BY Full_Name ASC";
        }else if(orderComboBox.getSelectedItem() == "Full Name: Z-A"){
            allEmployeesQuery = allEmployeesQuery + " ORDER BY Full_Name DESC";
        }

        try(
                    Statement selectAllEmployees = EmployeeSystem.connection.createStatement(ResultSet.TYPE_SCROLL_INSENSITIVE, ResultSet.CONCUR_READ_ONLY);
                    ResultSet employeesResultSet = selectAllEmployees.executeQuery(allEmployeesQuery);
                ){

            employeesTable.setModel(DbUtils.resultSetToTableModel(employeesResultSet));
            
            // Set the number of rows in the records label
            int numOfRows = employeesTable.getRowCount();
            numOfRecordsLabel.setText("" + numOfRows);
            
            // Set the last update time
            DateTimeFormatter dtFormat = DateTimeFormatter.ofPattern("yyyy/MM/dd HH:mm:ss");  
            LocalDateTime now = LocalDateTime.now();  
            lastUpdateLabel.setText(dtFormat.format(now));
            
        }catch(SQLException e){
            JOptionPane.showMessageDialog(null, e, "Error", JOptionPane.ERROR_MESSAGE);
        }
    }
    

