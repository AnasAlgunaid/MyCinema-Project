    private void searchButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchButtonActionPerformed
        String searchQuery = "SELECT Employee_ID AS `Employee ID`, Full_Name AS `Full Name`, Email, Role_Title AS `Role` FROM EmployeesInfo  ";
        
        // Search by movieID or Movie_Name
        if(searchComboBox.getSelectedItem() == "Employee ID"){
            // Check if the entered id is number
            boolean validID = true;
            int searchLength = searchTextField.getText().length();
            for(int i = 0; i < searchLength; i++){
                if(!Character.isDigit(searchTextField.getText().charAt(i))){
                    validID = false;
                    break;
                }
            }
            
            // Search if it is a number
            if(validID){
                searchQuery = searchQuery + "WHERE Employee_ID = \'" + searchTextField.getText() + "\' ";
            }else{
                JOptionPane.showMessageDialog(null, "Employee ID must be a number, Please enter a valid number", "Invalid Employee ID", JOptionPane.ERROR_MESSAGE);
                return;
            }
            
        }else if(searchComboBox.getSelectedItem() == "Full Name"){
            searchQuery = searchQuery + "WHERE Full_Name LIKE \'" + searchTextField.getText() +"%\'";
        }

        // Check for the order combobox 
        if(orderComboBox.getSelectedItem() == "Full Name: A-Z"){
            searchQuery = searchQuery + " ORDER BY Full_Name ASC";
        }else if(orderComboBox.getSelectedItem() == "Full Name: Z-A"){
            searchQuery = searchQuery + " ORDER BY Full_Name DESC";
        }
        
        // Get the result and refresh the table
        try(
            Statement getSearchResult = EmployeeSystem.connection.createStatement();
            ResultSet searchResultSet = getSearchResult.executeQuery(searchQuery);)
        {     
            // Refresh the table
            employeesTable.setModel(DbUtils.resultSetToTableModel(searchResultSet));
            
            // Update the number of rows in the records label
            int numOfRows = employeesTable.getRowCount();
            numOfRecordsLabel.setText("" + numOfRows);
        
        }catch(SQLException e){
            JOptionPane.showMessageDialog(null, e, "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_searchButtonActionPerformed

